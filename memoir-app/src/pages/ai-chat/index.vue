          console.log('AIÂõûÂ§çÂÜÖÂÆπ:', fullResponse);
          this.handleStreamResponse(fullResponse, messageIndex);
        } else {
          console.error('AIËÅäÂ§©ÂìçÂ∫îÈîôËØØ:', response.data);
          throw new Error(response.data.message || 'ËØ∑Ê±ÇÂ§±Ë¥•');
