    // Â§ÑÁêÜÊµÅÂºèÂìçÂ∫îÔºàÊ®°ÊãüÊâìÂ≠óÊú∫ÊïàÊûúÔºâ
    handleStreamResponse(fullResponse, messageIndex) {
      console.log('ÂºÄÂßãÊâìÂ≠óÊú∫ÊïàÊûúÔºåÂÜÖÂÆπÈïøÂ∫¶:', fullResponse.length);
      console.log('AIÂõûÂ§çÂÜÖÂÆπÈ¢ÑËßà:', fullResponse.substring(0, 100) + '...');
      
      // Áõ¥Êé•ÊõøÊç¢Êï¥‰∏™Ê∂àÊÅØÂØπË±°Êù•Á°Æ‰øùÂìçÂ∫îÂºèÊõ¥Êñ∞
      const updateMessage = (content) => {
